/**
 * Syncs images from ../Photos (UMBP/Photos) into public/photos and regenerates
 * src/data/cohortPhotos.generated.js for the interns page carousel.
 *
 * Add photos to: /Users/westonwatson/Documents/UMBP/Photos
 * Then run: npm run photos
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const root = path.join(fileURLToPath(import.meta.url), '..', '..')
const photosSource = path.join(root, '..', 'Photos')
const photosPublic = path.join(root, 'public', 'photos')
const outFile = path.join(root, 'src', 'data', 'cohortPhotos.generated.js')

const IMAGE_RE = /\.(jpe?g|png|webp|gif|avif)$/i

function listImages(dir) {
  if (!fs.existsSync(dir)) {
    console.warn(`Photos folder not found: ${dir}`)
    return []
  }
  return fs
    .readdirSync(dir)
    .filter((f) => IMAGE_RE.test(f) && !f.startsWith('.'))
    .sort((a, b) => a.localeCompare(b))
}

function altFromFilename(filename) {
  const base = filename.replace(IMAGE_RE, '')
  if (/^DSCF\d+/i.test(base) || /^[A-F0-9-]{20,}/i.test(base)) {
    return 'UMBP intern cohort'
  }
  const label = base.replace(/[_-]+/g, ' ').trim()
  return label ? `UMBP cohort — ${label}` : 'UMBP intern cohort'
}

if (!fs.existsSync(photosSource)) {
  console.warn('Create your Photos folder at:', photosSource)
  fs.mkdirSync(photosSource, { recursive: true })
}

const files = listImages(photosSource)
fs.mkdirSync(photosPublic, { recursive: true })

// Remove old synced images (keep .gitkeep)
for (const existing of fs.readdirSync(photosPublic)) {
  if (existing === '.gitkeep') continue
  if (IMAGE_RE.test(existing)) {
    fs.unlinkSync(path.join(photosPublic, existing))
  }
}

for (const file of files) {
  fs.copyFileSync(path.join(photosSource, file), path.join(photosPublic, file))
}

const manifest = files.map((file) => ({
  src: `/photos/${encodeURI(file)}`,
  alt: altFromFilename(file),
}))

const source = `// Auto-generated by "npm run photos" — do not edit
// Source folder: ../Photos

export const internCohortPhotos = ${JSON.stringify(manifest, null, 2)}
`

fs.writeFileSync(outFile, source)

console.log(`Synced ${files.length} photo(s) from Photos → public/photos`)
