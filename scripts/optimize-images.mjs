function rebuildPastEventPhotosFromWebp() {
  if (!fs.existsSync(eventsPublic)) return []

  const bySlug = new Map()

  for (const file of fs.readdirSync(eventsPublic)) {
    const match = file.match(/^(.+)-(\d+)w\.webp$/)
    if (!match) continue
    const [, slug, width] = match
    if (!bySlug.has(slug)) bySlug.set(slug, [])
    bySlug.get(slug).push({ width: Number(width), file })
  }

  const prefix = '/events'
  return PAST_EVENT_ORDER.filter((slug) => bySlug.has(slug)).map((slug) => {
    const entries = bySlug.get(slug).sort((a, b) => a.width - b.width)
    const primary = entries[entries.length - 1]
    return {
      id: slug,
      src: `${prefix}/${primary.file}`,
      srcSet: buildSrcSet(entries, prefix),
      sizes: EVENT_SIZES,
      width: primary.width,
      height: primary.height,
    }
  })
}

function writeStaticImagesManifest(applySummerProgramImage, ivoryCenterImage, pastEventPhotos) {
  const source = `// Auto-generated by "npm run photos" — do not edit

export const applySummerProgramImage = ${JSON.stringify(applySummerProgramImage, null, 2)}

export const ivoryCenterImage = ${JSON.stringify(ivoryCenterImage, null, 2)}

export const pastEventPhotos = ${JSON.stringify(pastEventPhotos, null, 2)}
`
  fs.writeFileSync(staticManifest, source)
}

async function optimizeCohortPhotos() {
  const sourceFiles = listImageFiles(photosSource)
  let inputs = []

  if (sourceFiles.length > 0) {
    inputs = sourceFiles.map((file) => ({
      file,
      inputPath: path.join(photosSource, file),
      slug: slugFromFilename(file),
      alt: altFromFilename(file),
    }))
  } else {
    const existing = listImageFiles(photosPublic).filter(isSourceImage)
    if (existing.length === 0) {
      const rebuilt = rebuildCohortManifestFromWebp()
      if (rebuilt.length) {
        writeJsModule(cohortManifest, 'internCohortPhotos', rebuilt)
        console.warn(`Rebuilt cohort manifest from ${rebuilt.length} existing WebP file(s)`)
        return rebuilt.length
      }
      console.warn('No cohort photos in ../Photos or public/photos/')
      const prior = readExistingManifest(cohortManifest, 'internCohortPhotos')
      if (prior?.length) return prior.length
      writeJsModule(cohortManifest, 'internCohortPhotos', [])
      return 0
    }
    console.warn(
      `../Photos not available — re-optimizing ${existing.length} file(s) in public/photos`,
    )
    inputs = existing.map((file) => ({
      file,
      inputPath: path.join(photosPublic, file),
      slug: slugFromFilename(file),
      alt: altFromFilename(file),
    }))
  }

  fs.mkdirSync(photosPublic, { recursive: true })
  const validSlugs = new Set()
  const manifest = []

  for (const { inputPath, slug, alt } of inputs) {
    validSlugs.add(slug)
    const { entries, primary } = await generateWebpVariants(
      inputPath,
      photosPublic,
      slug,
      COHORT_WIDTHS,
      { quality: 80, square: true },
    )

    const prefix = '/photos'
    manifest.push({
      src: `${prefix}/${primary.file}`,
      srcSet: buildSrcSet(entries, prefix),
      sizes: COHORT_SIZES,
      width: primary.width,
      height: primary.height,
      alt,
    })
  }

  removeStaleWebp(photosPublic, validSlugs)
  removeLegacyImages(photosPublic)

  writeJsModule(cohortManifest, 'internCohortPhotos', manifest)
  return manifest.length
}

async function optimizeInternPhotos() {
  const sourceFiles = listImageFiles(internsPublic).filter(isSourceImage)

  if (sourceFiles.length === 0) {
    const rebuilt = rebuildInternManifestFromWebp()
    const count = Object.keys(rebuilt).length
    if (count) {
      writeJsModule(internManifest, 'internPhotoAssets', rebuilt)
      console.warn(`Rebuilt intern manifest from ${count} existing WebP file(s)`)
      return count
    }
    const prior = readExistingManifest(internManifest, 'internPhotoAssets')
    if (prior && Object.keys(prior).length) {
      return Object.keys(prior).length
    }
    writeJsModule(internManifest, 'internPhotoAssets', {})
    return 0
  }

  const assets = {}
  const validSlugs = new Set()

  for (const file of sourceFiles) {
    const id = file.replace(/\.(jpe?g|png|gif|avif|webp)$/i, '').toLowerCase()
    validSlugs.add(id)

    const inputPath = path.join(internsPublic, file)
    const { entries, primary } = await generateWebpVariants(
      inputPath,
      internsPublic,
      id,
      INTERN_WIDTHS,
      { quality: 82, square: true },
    )

    const prefix = '/interns'
    assets[id] = {
      src: `${prefix}/${primary.file}`,
      srcSet: buildSrcSet(entries, prefix),
      sizes: INTERN_SIZES,
      modalSizes: INTERN_MODAL_SIZES,
      width: primary.width,
      height: primary.height,
    }
  }

  removeStaleWebp(internsPublic, validSlugs)
  removeLegacyImages(internsPublic)

  writeJsModule(internManifest, 'internPhotoAssets', assets)
  return Object.keys(assets).length
}

async function optimizeApplyImage() {
  const outputDir = path.join(root, 'public')
  const inputPath = ['png', 'jpg', 'jpeg']
    .map((ext) => path.join(outputDir, `apply-summer-program.${ext}`))
    .find((candidate) => fs.existsSync(candidate))

  if (!inputPath) {
    const rebuilt = rebuildApplyManifestFromWebp()
    if (rebuilt) return rebuilt
    const prior = readExistingManifest(staticManifest, 'applySummerProgramImage')
    if (prior) return prior
    console.warn('apply-summer-program source not found — skipping')
    return null
  }

  for (const file of fs.readdirSync(outputDir)) {
    if (file.startsWith('apply-summer-program-') && file.endsWith('.webp')) {
      fs.unlinkSync(path.join(outputDir, file))
    }
  }

  const { entries, primary } = await generateWebpVariants(
    inputPath,
    outputDir,
    'apply-summer-program',
    APPLY_WIDTHS,
    { quality: 85, maxWidth: 1200 },
  )

  return {
    src: `/${primary.file}`,
    srcSet: buildSrcSet(entries, ''),
    sizes: APPLY_SIZES,
    width: primary.width,
    height: primary.height,
  }
}

