/**
 * Optimizes site images: cohort carousel, intern headshots, apply section.
 * Sources cohort originals from ../Photos when available; otherwise re-processes public/photos.
 *
 * Run: npm run photos
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import {
  buildSrcSet,
  generateWebpVariants,
  isSourceImage,
  listImageFiles,
  removeLegacyImages,
  removeStaleWebp,
  slugFromFilename,
} from './lib/image-optimize.mjs'

const root = path.join(fileURLToPath(import.meta.url), '..', '..')
const photosSource = path.join(root, '..', 'Photos')
const photosPublic = path.join(root, 'public', 'photos')
const internsPublic = path.join(root, 'public', 'interns')
const eventsPublic = path.join(root, 'public', 'events')
const cohortManifest = path.join(root, 'src', 'data', 'cohortPhotos.generated.js')
const internManifest = path.join(root, 'src', 'data', 'internPhotos.generated.js')
const staticManifest = path.join(root, 'src', 'data', 'staticImages.generated.js')

const COHORT_WIDTHS = [280, 560]
const INTERN_WIDTHS = [400, 800]
const APPLY_WIDTHS = [600, 900, 1200]
const IVORY_CENTER_WIDTHS = [480, 720, 1024]
const EVENT_WIDTHS = [400, 696, 1200]

const COHORT_SIZES = '(max-width: 640px) 180px, (max-width: 768px) 220px, 280px'
const INTERN_SIZES = '(max-width: 640px) 45vw, (max-width: 1024px) 22vw, 260px'
const INTERN_MODAL_SIZES = '(max-width: 520px) 100vw, 520px'
const APPLY_SIZES = '(max-width: 768px) 100vw, 50vw'
const IVORY_CENTER_SIZES = '(min-width: 768px) 520px, 100vw'
const EVENT_SIZES = '(min-width: 768px) 33vw, 100vw'

const PAST_EVENT_ORDER = [
  'build-with-ai-workshop',
  'code-to-success-morning',
  'social-media-101-workshop',
  'umbp-interns-2017',
  'commencement-dinner',
  'on-the-ground-moldova',
]

function altFromFilename(filename) {
  const IMAGE_RE = /\.(jpe?g|png|webp|gif|avif)$/i
  const base = filename.replace(IMAGE_RE, '')
  if (/^DSCF\d+/i.test(base) || /^[A-F0-9-]{20,}/i.test(base)) {
    return 'UMBP intern cohort'
  }
  const label = base.replace(/[_-]+/g, ' ').trim()
  return label ? `UMBP cohort — ${label}` : 'UMBP intern cohort'
}

function writeJsModule(filePath, exportName, value) {
  const source = `// Auto-generated by "npm run photos" — do not edit

export const ${exportName} = ${JSON.stringify(value, null, 2)}
`
  fs.writeFileSync(filePath, source)
}

function readExistingManifest(filePath, exportName) {
  if (!fs.existsSync(filePath)) return null
  const text = fs.readFileSync(filePath, 'utf8')
  const match = text.match(new RegExp(`export const ${exportName} = ([\\s\\S]+)$`))
  if (!match) return null
  try {
    return JSON.parse(match[1])
  } catch {
    return null
  }
}

function rebuildCohortManifestFromWebp() {
  const bySlug = new Map()

  for (const file of fs.readdirSync(photosPublic)) {
    const match = file.match(/^(.+)-(\d+)w\.webp$/)
    if (!match) continue
    const [, slug, width] = match
    if (!bySlug.has(slug)) bySlug.set(slug, [])
    bySlug.get(slug).push({ width: Number(width), file })
  }

  return [...bySlug.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([slug, entries]) => {
      entries.sort((a, b) => a.width - b.width)
      const primary = entries[entries.length - 1]
      const prefix = '/photos'
      return {
        src: `${prefix}/${primary.file}`,
        srcSet: buildSrcSet(entries, prefix),
        sizes: COHORT_SIZES,
        width: primary.width,
        height: primary.width,
        alt: altFromFilename(`${slug}.jpg`),
      }
    })
}

function rebuildInternManifestFromWebp() {
  const assets = {}

  for (const file of fs.readdirSync(internsPublic)) {
    const match = file.match(/^(.+)-(\d+)w\.webp$/)
    if (!match) continue
    const [, id, width] = match
    if (!assets[id]) assets[id] = []
    assets[id].push({ width: Number(width), file })
  }

  const result = {}
  for (const [id, entries] of Object.entries(assets)) {
    entries.sort((a, b) => a.width - b.width)
    const primary = entries[entries.length - 1]
    const prefix = '/interns'
    result[id] = {
      src: `${prefix}/${primary.file}`,
      srcSet: buildSrcSet(entries, prefix),
      sizes: INTERN_SIZES,
      modalSizes: INTERN_MODAL_SIZES,
      width: primary.width,
      height: primary.width,
    }
  }
  return result
}

function rebuildApplyManifestFromWebp() {
  const outputDir = path.join(root, 'public')
  const entries = []

  for (const file of fs.readdirSync(outputDir)) {
    const match = file.match(/^apply-summer-program-(\d+)w\.webp$/)
    if (!match) continue
    entries.push({ width: Number(match[1]), file })
  }

  if (!entries.length) return null

  entries.sort((a, b) => a.width - b.width)
  const primary = entries[entries.length - 1]
  return {
    src: `/${primary.file}`,
    srcSet: buildSrcSet(entries, ''),
    sizes: APPLY_SIZES,
    width: primary.width,
    height: primary.height,
  }
}

