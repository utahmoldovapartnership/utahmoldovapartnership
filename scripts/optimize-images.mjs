async function optimizePastEventPhotos() {
  if (!fs.existsSync(eventsPublic)) {
    const rebuilt = rebuildPastEventPhotosFromWebp()
    if (rebuilt.length) return rebuilt
    const prior = readExistingManifest(staticManifest, 'pastEventPhotos')
    if (prior?.length) return prior
    return []
  }

  const sourceFiles = listImageFiles(eventsPublic).filter(isSourceImage)
  if (sourceFiles.length === 0) {
    const rebuilt = rebuildPastEventPhotosFromWebp()
    if (rebuilt.length) return rebuilt
    const prior = readExistingManifest(staticManifest, 'pastEventPhotos')
    if (prior?.length) return prior
    return []
  }

  const validSlugs = new Set(PAST_EVENT_ORDER)

  for (const file of sourceFiles) {
    const id = file.replace(/\.(jpe?g|png|gif|avif|webp)$/i, '').toLowerCase()
    validSlugs.add(id)

    const inputPath = path.join(eventsPublic, file)
    await generateWebpVariants(
      inputPath,
      eventsPublic,
      id,
      EVENT_WIDTHS,
      { quality: 85, maxWidth: 1200 },
    )
  }

  removeStaleWebp(eventsPublic, validSlugs)
  removeLegacyImages(eventsPublic)

  return rebuildPastEventPhotosFromWebp()
}

const cohortCount = await optimizeCohortPhotos()
const internCount = await optimizeInternPhotos()
const applyImage = await optimizeApplyImage()
const ivoryCenterImage = await optimizeIvoryCenterImage()
const pastEventPhotos = await optimizePastEventPhotos()
writeStaticImagesManifest(applyImage, ivoryCenterImage, pastEventPhotos)

console.log(
  `Optimized ${cohortCount} cohort photo(s), ${internCount} intern headshot(s), apply image, ivory center image, ${pastEventPhotos.length} past event photo(s).`,
)
